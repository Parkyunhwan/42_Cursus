#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return ((char*)s);
		s++;
	}
	if (!c)
		return ((char*)s);
	return (0);
}
/*
** if c is null character -> return (address) + length (word end address)
*/
